# ────────────────────────────────────────────────────────────
# TRANSFORMS
# ────────────────────────────────────────────────────────────
img_tf = T.Compose([
    T.ToTensor(),
    T.Normalize([0.5]*3,[0.5]*3)
])

def unnorm(t):
    return (t*0.5+0.5).clamp(0,1)

# ============================================================
# SSIM
# ============================================================
def ssim_np(a, b):
    C1, C2 = 0.01**2, 0.03**2
    a = a.astype(np.float64)
    b = b.astype(np.float64)

    mu_a = cv2.GaussianBlur(a, (11,11), 1.5)
    mu_b = cv2.GaussianBlur(b, (11,11), 1.5)

    mu_a2 = mu_a**2
    mu_b2 = mu_b**2
    mu_ab = mu_a * mu_b

    sigma_a = cv2.GaussianBlur(a*a,(11,11),1.5) - mu_a2
    sigma_b = cv2.GaussianBlur(b*b,(11,11),1.5) - mu_b2
    sigma_ab= cv2.GaussianBlur(a*b,(11,11),1.5) - mu_ab

    ssim_map = ((2*mu_ab + C1)*(2*sigma_ab + C2)) / \
               ((mu_a2 + mu_b2 + C1)*(sigma_a + sigma_b + C2))

    return float(ssim_map.mean())

# ============================================================
# DUAL MASK
# ============================================================
def make_mask(pil_img):

    img  = np.array(pil_img.convert('RGB'))
    gray = cv2.cvtColor(img, cv2.COLOR_RGB2GRAY)

    # --- STRONG MASK (RESTORE) ---
    bright = gray > 200
    faded  = (gray > 150) & (gray < 210)

    blur = cv2.GaussianBlur(gray, (21,21), 0)
    texture = np.abs(gray - blur) > 20

    mask_restore = (bright | (faded & texture)).astype(np.uint8) * 255

    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE,(11,11))
    mask_restore = cv2.dilate(mask_restore, kernel, iterations=2)

    mask_restore = cv2.GaussianBlur(mask_restore, (9,9), 0)
    mask_restore = (mask_restore > 80).astype(np.uint8) * 255

    # --- CLEAN MASK (DISPLAY) ---
    bright_d = (gray > 200)
    dark_d   = (gray < 60)

    mask_display = (bright_d | dark_d).astype(np.uint8) * 255

    k = cv2.getStructuringElement(cv2.MORPH_ELLIPSE,(5,5))
    mask_display = cv2.morphologyEx(mask_display, cv2.MORPH_OPEN, k)
    mask_display = cv2.morphologyEx(mask_display, cv2.MORPH_CLOSE, k)

    if mask_display.sum() == 0:
        edges = cv2.Canny(gray, 50, 150)
        mask_display = (edges > 0).astype(np.uint8) * 255

    H,W = gray.shape
    cov_restore = mask_restore.sum()/(H*W*255)*100
    cov_display = mask_display.sum()/(H*W*255)*100

    return mask_restore, mask_display, cov_restore, cov_display

# ============================================================
# RESTORE (CELL 6 EXACT)
# ============================================================
def restore(pil_img, mask255):

    W,H = pil_img.size
    orig_np = np.array(pil_img.convert('RGB')).astype(np.float32)

    mask_pil = Image.fromarray(mask255).convert('L')
    lama_out = simple_lama(pil_img, mask_pil)
    lama_np  = np.array(lama_out).astype(np.float32)

    lama_256 = lama_out.resize((256,256), Image.LANCZOS)
    orig_256 = pil_img.resize((256,256), Image.LANCZOS)
    mk_256   = cv2.resize(mask255,(256,256), interpolation=cv2.INTER_NEAREST)

    lama_t = img_tf(lama_256).unsqueeze(0).to(device)
    orig_t = img_tf(orig_256).unsqueeze(0).to(device)

    msk_t = torch.from_numpy(
        (mk_256 < 128).astype(np.float32)
    ).unsqueeze(0).unsqueeze(0).to(device)

    with torch.no_grad():
        fused_t = ensemble_restore(orig_t, msk_t)

    fused_np = unnorm(fused_t).cpu().squeeze(0).permute(1,2,0).numpy()
    fused_np = (fused_np*255).astype(np.uint8)

    fused_full = np.array(
        Image.fromarray(fused_np).resize((W,H), Image.LANCZOS)
    ).astype(np.float32)

    combined = (lama_np*0.6 + fused_full*0.4)

    mask_f = mask255.astype(np.float32)/255.0
    mask_f = cv2.GaussianBlur(mask_f,(31,31),0)[:,:,None]

    result = (orig_np*(1-mask_f) + combined*mask_f).clip(0,255)

    return Image.fromarray(result.astype(np.uint8))

