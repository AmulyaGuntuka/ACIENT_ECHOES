def detect_real_damage(path):
    img  = np.array(Image.open(path).convert('RGB'))
    gray = cv2.cvtColor(img, cv2.COLOR_RGB2GRAY)
    hsv  = cv2.cvtColor(img, cv2.COLOR_RGB2HSV)
    H, W = gray.shape
    m_bright = ((gray>185)&(hsv[:,:,1]<55)).astype(np.uint8)*255
    m_dark   = ((gray< 45)&(hsv[:,:,1]<55)).astype(np.uint8)*255
    m = cv2.bitwise_or(m_bright, m_dark)
    k = cv2.getStructuringElement(cv2.MORPH_ELLIPSE,(7,7))
    m = cv2.morphologyEx(m, cv2.MORPH_CLOSE, k)
    m = cv2.morphologyEx(m, cv2.MORPH_OPEN,
        cv2.getStructuringElement(cv2.MORPH_ELLIPSE,(3,3)))
    n,labels,stats,_ = cv2.connectedComponentsWithStats(m)
    out = np.zeros_like(m)
    for i in range(1,n):
        if stats[i,cv2.CC_STAT_AREA] >= int(H*W*0.002):
            out[labels==i] = 255
    cov = out.sum()/(H*W*255)*100
    return cov >= 3.0, out, cov

def make_small_damage(h, w):
    mask = np.zeros((h,w), np.uint8)
    for _ in range(random.randint(2,4)):
        x,y = random.randint(0,w), random.randint(0,h)
        for _ in range(random.randint(3,8)):
            angle = random.uniform(0, 2*np.pi)
            l = random.randint(5,20)
            x2=int(np.clip(x+l*np.cos(angle),0,w-1))
            y2=int(np.clip(y+l*np.sin(angle),0,h-1))
            cv2.line(mask,(x,y),(x2,y2),255,random.randint(1,3))
            x,y=x2,y2
    for _ in range(random.randint(5,15)):
        cx,cy=random.randint(5,w-5),random.randint(5,h-5)
        cv2.circle(mask,(cx,cy),random.randint(2,6),255,-1)
    cov = mask.sum()/(h*w*255)
    if cov > 0.05:
        mask=(mask.astype(float)*(0.05/cov)).clip(0,255).astype(np.uint8)
    return mask

