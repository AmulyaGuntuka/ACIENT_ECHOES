# Sobel edge loss — critical for pattern recovery
def edge_loss(pred, target):
    def sobel(t):
        kx = torch.tensor([[-1,0,1],[-2,0,2],[-1,0,1]],
                           dtype=torch.float32,device=t.device
                           ).view(1,1,3,3).repeat(3,1,1,1)
        ky = torch.tensor([[-1,-2,-1],[0,0,0],[1,2,1]],
                           dtype=torch.float32,device=t.device
                           ).view(1,1,3,3).repeat(3,1,1,1)
        ex = F.conv2d(t, kx, padding=1, groups=3)
        ey = F.conv2d(t, ky, padding=1, groups=3)
        return torch.sqrt(ex**2+ey**2+1e-6)
    return F.l1_loss(sobel(pred), sobel(target))

# FFT frequency loss — recovers fine texture patterns
def freq_loss(pred, target):
    fp = torch.fft.rfft2(pred,  norm='ortho')
    ft = torch.fft.rfft2(target,norm='ortho')
    return F.l1_loss(fp.real,ft.real) + F.l1_loss(fp.imag,ft.imag)

def gram(f):
    b,c,h,w=f.shape; f=f.view(b,c,-1)
    return torch.bmm(f,f.transpose(1,2))/(c*h*w)

def unnorm(t): return (t*0.5+0.5).clamp(0,1)

def compute_loss(pred, target, mask, lama_ref):
    hole   = 1 - mask
    n_hole = hole.sum().clamp(min=1)
    n_valid= mask.sum().clamp(min=1)

    # ── L1 losses ──
    # Valid region (intact) — light penalty
    l_valid  = (F.l1_loss(pred*mask, target*mask, reduction='sum')
                / n_valid) * 1.0

    # Hole region — HEAVY penalty, this is where restoration happens
    l_hole   = (F.l1_loss(pred*hole, target*hole, reduction='sum')
                / n_hole) * 10.0

    # Residual vs LaMa — push refiner to actually CHANGE LaMa output
    # penalize if refiner just copies LaMa in hole regions
    l_residual = (F.l1_loss((pred - lama_ref)*hole,
                             (target - lama_ref)*hole,
                             reduction='sum') / n_hole) * 4.0

    # ── Perceptual ──
    fp = vgg_loss(unnorm(pred).float())
    ft = vgg_loss(unnorm(target).float())
    l_perc = sum(F.l1_loss(a,b) for a,b in zip(fp,ft)) * 0.1

    # ── Style (Gram) — pattern texture recovery ──
    l_style = sum(F.l1_loss(gram(a),gram(b))
                  for a,b in zip(fp,ft)) * 80.0

    # ── Edge loss — sharp pattern boundaries ──
    l_edge = edge_loss(unnorm(pred).float(),
                       unnorm(target).float()) * 2.0

    # ── Frequency loss — fine Dunhuang patterns ──
    l_freq = freq_loss(pred, target) * 0.5

    total = (l_valid + l_hole + l_residual +
             l_perc + l_style + l_edge + l_freq)

    return total, {
        'valid':l_valid.item(),'hole':l_hole.item(),
        'resid':l_residual.item(),'perc':l_perc.item(),
        'style':l_style.item(),'edge':l_edge.item(),
        'freq':l_freq.item()
    }

