# ── Gated Conv block ──────────────────────────────────────
class GatedConv(nn.Module):
    def __init__(self, in_ch, out_ch, k=3, s=1, d=1):
        super().__init__()
        p = d*(k//2)
        self.conv  = nn.Conv2d(in_ch, out_ch, k, s, p, dilation=d)
        self.gate  = nn.Conv2d(in_ch, out_ch, k, s, p, dilation=d)
        self.bn    = nn.BatchNorm2d(out_ch)
    def forward(self, x):
        return self.bn(self.conv(x) * torch.sigmoid(self.gate(x)))

# ── DunhuangRefiner v2 — Residual Correction ──────────────
class DunhuangRefiner(nn.Module):
    """
    Input : lama_out (3) + orig_img (3) + mask (1) = 7ch
    Output: lama_out + learned_correction * hole_mask
    Residual design: refiner predicts DIFFERENCE from LaMa
    This forces it to learn what LaMa got wrong, not repeat it
    """
    def __init__(self):
        super().__init__()
        # Encoder — gated convs for hole-awareness
        self.e1 = GatedConv(7,  64)        # [B,64,H,W]
        self.e2 = GatedConv(64, 128, s=2)  # [B,128,H/2,W/2]
        self.e3 = GatedConv(128,256, s=2)  # [B,256,H/4,W/4]
        self.e4 = GatedConv(256,256, s=2)  # [B,256,H/8,W/8]

        # Dilated bottleneck — large receptive field for patterns
        self.d1 = GatedConv(256,256, d=2)
        self.d2 = GatedConv(256,256, d=4)
        self.d3 = GatedConv(256,256, d=8)
        self.d4 = GatedConv(256,256, d=16)
        self.dfuse = nn.Conv2d(256*4, 256, 1)

        # Decoder with skip connections
        self.up3  = nn.Upsample(scale_factor=2,mode='bilinear',align_corners=False)
        self.dec3 = GatedConv(256+256, 256)

        self.up2  = nn.Upsample(scale_factor=2,mode='bilinear',align_corners=False)
        self.dec2 = GatedConv(256+128, 128)

        self.up1  = nn.Upsample(scale_factor=2,mode='bilinear',align_corners=False)
        self.dec1 = GatedConv(128+64, 64)

        # Residual correction head — outputs [-1,1] correction
        self.corr = nn.Sequential(
            nn.Conv2d(64, 32, 3, padding=1),
            nn.ReLU(inplace=True),
            nn.Conv2d(32, 3,  3, padding=1),
            nn.Tanh()
        )

        # Confidence head — how much to trust correction vs LaMa
        self.conf = nn.Sequential(
            nn.Conv2d(64, 32, 3, padding=1),
            nn.ReLU(inplace=True),
            nn.Conv2d(32, 1,  3, padding=1),
            nn.Sigmoid()
        )

    def forward(self, lama_out, orig_img, mask):
        # mask: 1=intact 0=damaged
        hole = 1 - mask
        x  = torch.cat([lama_out, orig_img, mask], dim=1)

        e1 = self.e1(x)
        e2 = self.e2(e1)
        e3 = self.e3(e2)
        e4 = self.e4(e3)

        # Dilated bottleneck
        b = torch.cat([self.d1(e4),self.d2(e4),
                       self.d3(e4),self.d4(e4)], dim=1)
        b = F.relu(self.dfuse(b), inplace=True)

        # Decoder
        d3 = self.dec3(torch.cat([self.up3(b),  e3], dim=1))
        d2 = self.dec2(torch.cat([self.up2(d3), e2], dim=1))
        d1 = self.dec1(torch.cat([self.up1(d2), e1], dim=1))

        corr = self.corr(d1)   # residual correction [-1,1]
        conf = self.conf(d1)   # confidence [0,1]

        # Apply correction only to hole regions
        # lama_out is our base — we add a confident correction
        corrected = (lama_out + corr * 0.5).clamp(-1, 1)
        blended   = conf * corrected + (1-conf) * lama_out

        # Composite: intact pixels stay original
        return orig_img * mask + blended * hole

