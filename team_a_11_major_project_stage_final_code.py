def ensemble_restore(damaged_t, mask_t):
    outputs=[]; weights=[]
    with torch.no_grad():
        lama_t = lama_inpaint_batch(damaged_t, mask_t)
        outputs.append(lama_t); weights.append(1.5)

        if loaded.get('gen_best'):
            o = gen_best(damaged_t, mask_t).clamp(-1,1)
            outputs.append(o); weights.append(1.5)

        if loaded.get('gen_ep60'):
            o = gen_ep60(damaged_t, mask_t).clamp(-1,1)
            outputs.append(o); weights.append(1.0)

        if loaded.get('gen_best') and loaded.get('ref_net'):
            coarse = gen_best(damaged_t, mask_t).clamp(-1,1)
            o = ref_net(coarse, damaged_t, mask_t).clamp(-1,1)
            outputs.append(o); weights.append(1.0)

        if loaded.get('old_dun'):
            o = old_dun(lama_t, damaged_t, mask_t).clamp(-1,1)
            outputs.append(o); weights.append(1.0)

        if loaded.get('new_ref'):
            o = new_ref(lama_t, damaged_t, mask_t).clamp(-1,1)
            outputs.append(o); weights.append(4.0)

    fused = sum(o*w for o,w in zip(outputs,weights)) / sum(weights)
    hole  = 1 - mask_t
    return (damaged_t*mask_t + fused.clamp(-1,1)*hole)

