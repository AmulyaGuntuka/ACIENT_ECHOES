class CBAM(nn.Module):
    def __init__(self,c,r=16):
        super().__init__()
        self.ca=nn.Sequential(
            nn.AdaptiveAvgPool2d(1),nn.Flatten(),
            nn.Linear(c,c//r),nn.ReLU(),nn.Linear(c//r,c),nn.Sigmoid())
        self.sa=nn.Sequential(nn.Conv2d(2,1,7,padding=3),nn.Sigmoid())
    def forward(self,x):
        w=self.ca(x).view(x.shape[0],x.shape[1],1,1); x=x*w
        m=torch.cat([x.mean(1,keepdim=True),x.max(1,keepdim=True)[0]],1)
        return x*self.sa(m)

class AttGate(nn.Module):
    def __init__(self,g,x,i=128):
        super().__init__()
        self.wg=nn.Conv2d(g,i,1); self.wx=nn.Conv2d(x,i,1)
        self.psi=nn.Sequential(nn.Conv2d(i,1,1),nn.Sigmoid())
    def forward(self,g,x):
        wg = self.wg(g)
        wx = self.wx(x)
        if wg.shape != wx.shape:
            wg = F.interpolate(wg, wx.shape[2:],
                               mode='bilinear', align_corners=False)
        return x * self.psi(F.relu(wg + wx, True))

class GCB(nn.Module):
    def __init__(self,i,o,k=3,s=1,p=1,d=1):
        super().__init__()
        self.c=nn.Conv2d(i,o,k,s,p,dilation=d)
        self.g=nn.Conv2d(i,o,k,s,p,dilation=d)
        self.bn=nn.BatchNorm2d(o)
    def forward(self,x):
        return F.relu(self.bn(self.c(x)*torch.sigmoid(self.g(x))),True)

class RestorationNet(nn.Module):
    def __init__(self):
        super().__init__()
        enc=tvm.resnet34(weights=None)
        self.e0=nn.Sequential(enc.conv1,enc.bn1,enc.relu)
        self.pool=enc.maxpool
        self.e1=enc.layer1; self.e2=enc.layer2
        self.e3=enc.layer3; self.e4=enc.layer4
        self.mid=nn.Sequential(
            GCB(512,512,3,1,2,2),GCB(512,512,3,1,4,4),
            GCB(512,512,3,1,8,8),GCB(512,512,3,1,16,16))
        def up(ic,sc,oc):
            return nn.ModuleDict({'att':AttGate(ic,sc),'cbam':CBAM(sc),
                'up':nn.ConvTranspose2d(ic,oc,4,2,1),'gate':GCB(oc+sc,oc)})
        self.u4=up(512,256,256); self.u3=up(256,128,128)
        self.u2=up(128,64,64);   self.u1=up(64,64,64)
        self.out=nn.Sequential(nn.Conv2d(64,3,3,1,1),nn.Tanh())
    def _up(self,blk,g,x):
        x2=blk['cbam'](blk['att'](g,x))
        g2=blk['up'](g)
        if g2.shape!=x2.shape:
            g2=F.interpolate(g2,x2.shape[2:],mode='bilinear',align_corners=False)
        return blk['gate'](torch.cat([g2,x2],1))
    def forward(self,x,mask):
        xi=x*mask
        e0=self.e0(xi); ep=self.pool(e0)
        e1=self.e1(ep); e2=self.e2(e1)
        e3=self.e3(e2); e4=self.e4(e3)
        m=sum(self.mid[i](e4) for i in range(4))
        d=self._up(self.u4,m,e3); d=self._up(self.u3,d,e2)
        d=self._up(self.u2,d,e1); d=self._up(self.u1,d,e0)
        if d.shape!=xi.shape:
            d=F.interpolate(d,xi.shape[2:],mode='bilinear',align_corners=False)
        return xi*mask+self.out(d)*(1-mask)

class RefinementNet(nn.Module):
    def __init__(self):
        super().__init__()
        def gc(i,o,k=4,s=2,p=1,d=1):
            return nn.ModuleDict({'c':nn.Conv2d(i,o,k,s,p,dilation=d),
                'g':nn.Conv2d(i,o,k,s,p,dilation=d),'n':nn.InstanceNorm2d(o)})
        self.e1=gc(7,64);    self.e2=gc(64,128)
        self.e3=gc(128,256); self.e4=gc(256,512)
        # ── FIX: input must be 512 (e4 output) ───────────
        self.d1=gc(512,512,3,1,2,2); self.d2=gc(512,512,3,1,4,4)
        self.d3=gc(512,512,3,1,8,8)
        def up(i,o):
            return nn.Sequential(nn.ConvTranspose2d(i,o,4,2,1),
                                  nn.InstanceNorm2d(o),nn.ReLU(True))
        self.u1=up(512,256); self.u2=up(256+256,128)
        self.u3=up(128+128,64); self.u4=up(64+64,32)
        self.out=nn.Sequential(nn.Conv2d(32,3,3,1,1),nn.Tanh())
    def _a(self,m,x):
        return F.leaky_relu(m['n'](m['c'](x)*torch.sigmoid(m['g'](x))),0.2,True)
    def forward(self,coarse,orig,mask):
        x=torch.cat([coarse,orig,mask],1)
        e1=self._a(self.e1,x); e2=self._a(self.e2,e1)
        e3=self._a(self.e3,e2); e4=self._a(self.e4,e3)
        b=e4
        for dl in [self.d1,self.d2,self.d3]:
            b=F.leaky_relu(dl['n'](dl['c'](b)*torch.sigmoid(dl['g'](b))),0.2,True)
        d=self.u1(b)
        if d.shape[2:]!=e3.shape[2:]: d=F.interpolate(d,e3.shape[2:])
        d=self.u2(torch.cat([d,e3],1))
        if d.shape[2:]!=e2.shape[2:]: d=F.interpolate(d,e2.shape[2:])
        d=self.u3(torch.cat([d,e2],1))
        if d.shape[2:]!=e1.shape[2:]: d=F.interpolate(d,e1.shape[2:])
        d=self.u4(torch.cat([d,e1],1))
        if d.shape[2:]!=orig.shape[2:]: d=F.interpolate(d,orig.shape[2:])
        return self.out(d)

